import uka.transport.Transportable;

/**
 * This interface is a set of callbacks that the stack uses to notify the application of
 * replies to previous request. A callback object is passed as an argument  
 * upon the creation of each {@link seqSamoa.ProtocolSatck stack}.
 */
public interface Callback {
    public void serviceCallback(Object infos, Transportable message);
}
